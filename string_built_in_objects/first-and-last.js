/*2. Get the First and Last Character
Problem: Return the first and last character of a string.

Hint: Use charAt(0) and charAt(str.length - 1).

Sample Input: "coding"

Sample Output: ["c", "g"]
*/

let message = "Shittal's brother"
console.log("First character of the given string is: " + message.charAt(0));
console.log("Last character of the given string is: " + message.charAt(message.length - 1));
